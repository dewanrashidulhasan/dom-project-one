const body = document.getElementById('body'); 
// const: এটি একটি কনস্ট্যান্ট ভেরিয়েবল ডিক্লেয়ার করার জন্য ব্যবহৃত হয়। এর মান পরে পরিবর্তন করা যাবে না।
// body: এটি একটি ভেরিয়েবল, যেখানে HTML ডকুমেন্ট থেকে 'body' আইডি-যুক্ত element সংরক্ষণ করা হয়েছে।
// document: এটি পুরো HTML ডকুমেন্টকে নির্দেশ করে, যার মাধ্যমে আমরা DOM (Document Object Model) অ্যাক্সেস করতে পারি।
// getElementById: এটি একটি মেথড, যা নির্দিষ্ট আইডি-যুক্ত HTML element খুঁজে বের করে এবং return করে।

const button = document.querySelectorAll('li'); 
// const: পূর্বের মতো, একটি কনস্ট্যান্ট ভেরিয়েবল ডিক্লেয়ার করার জন্য ব্যবহৃত হয়েছে।
// button: এটি একটি ভেরিয়েবল, যেখানে সমস্ত <li> element গুলো NodeList হিসেবে সংরক্ষণ করা হয়েছে।
// document: এটি HTML ডকুমেন্টকে নির্দেশ করে।
// querySelectorAll: এটি একটি মেথড, যা নির্দিষ্ট CSS সিলেক্টর অনুযায়ী একাধিক element খুঁজে বের করে এবং একটি NodeList return করে। এখানে এটি সমস্ত <li> element গুলো খুঁজেছে।

button.forEach(function (value) { 
// button: পূর্বে সংরক্ষিত NodeList, যা <li> element গুলোর সমষ্টি।
// forEach: এটি একটি মেথড, যা NodeList-এর প্রতিটি element-এর উপর একটি লুপ চালায়।
// function: প্রতিটি element-এর জন্য এটি একটি কলব্যাক ফাংশন হিসেবে কাজ করে।
// value: প্রতিটি <li> element-কে একটি একক প্যারামিটার হিসেবে ধরে।

  value.addEventListener('click', function () { 
  // value: এটি বর্তমান <li> element-কে নির্দেশ করে, যেটিতে ইভেন্ট লিসেনার যুক্ত হচ্ছে।
  // addEventListener: এটি একটি মেথড, যা নির্দিষ্ট ইভেন্টের জন্য একটি ফাংশন বা কলব্যাক সংযুক্ত করে। এখানে 'click' ইভেন্টের জন্য কলব্যাক ফাংশন যোগ করা হয়েছে।
  // 'click': এটি ইভেন্ট টাইপ, যা নির্দিষ্ট element-এ ক্লিক করলে ট্রিগার হবে।
  // function: এটি একটি অ্যাকশন নির্ধারণ করে, যা ইভেন্টটি ট্রিগার হলে চালানো হবে।

    let className = this.classList[0]; 
    // let: একটি ব্লক-স্কোপড ভেরিয়েবল ডিক্লেয়ার করার জন্য ব্যবহৃত হয়েছে। 
    // className: ভেরিয়েবলে বর্তমান <li> element-এর প্রথম ক্লাসটি সংরক্ষণ করা হয়েছে।
    // this: এটি সেই element-কে নির্দেশ করে, যেখানে ইভেন্টটি ঘটেছে। অর্থাৎ, যে <li> element ক্লিক করা হয়েছে।
    // classList: এটি একটি প্রপার্টি, যা element-এর সমস্ত ক্লাসের একটি তালিকা প্রদান করে।
    // [0]: ক্লাস তালিকার প্রথম ক্লাসটি নির্বাচন করা হয়েছে।

    let color = ''; 
    // let: একটি ব্লক-স্কোপড ভেরিয়েবল ডিক্লেয়ার করা হয়েছে।
    // color: এটি একটি ভেরিয়েবল, যেখানে ব্যাকগ্রাউন্ড রঙের মান সংরক্ষণ করা হবে।
    // '': এটি একটি খালি স্ট্রিং দিয়ে ভেরিয়েবল ইনিশিয়ালাইজ করা হয়েছে।

    if (className === 'red') { 
      color = '#ff7675'; 
    } 
    // if: একটি শর্ত নির্ধারণ করার জন্য ব্যবহৃত হয়েছে। 
    // className === 'red': চেক করা হয়েছে, className-এর মান কি 'red'। 
    // color = '#ff7675': যদি শর্তটি সত্য হয়, তবে color ভেরিয়েবলে লাল রঙের হেক্সাডেসিমাল কোড সেট করা হয়েছে।

    if (className === 'green') { 
      color = '#55efc4'; 
    } 
    // একইভাবে, যদি className-এর মান 'green' হয়, তবে সবুজ রঙ সেট করা হবে।

    if (className === 'blue') { 
      color = '#74b9ff'; 
    } 
    // className-এর মান 'blue' হলে নীল রঙ সেট করা হবে।

    if (className === 'pink') { 
      color = '#fd79a8'; 
    } 
    // className-এর মান 'pink' হলে গোলাপি রঙ সেট করা হবে।

    if (className === 'purple') { 
      color = '#a29bfe'; 
    } 
    // className-এর মান 'purple' হলে বেগুনি রঙ সেট করা হবে।

    body.style.backgroundColor = color; 
    // body: এটি পূর্বে ডিক্লেয়ার করা body ভেরিয়েবল, যা পুরো HTML ডকুমেন্টের body অংশকে নির্দেশ করে।
    // style: এটি একটি প্রপার্টি, যা element-এর CSS স্টাইল পরিবর্তন করতে ব্যবহৃত হয়।
    // backgroundColor: এটি একটি CSS প্রপার্টি, যা element-এর ব্যাকগ্রাউন্ড রঙ নির্ধারণ করে।
    // color: উপরের শর্ত অনুযায়ী নির্ধারিত রঙটি ব্যাকগ্রাউন্ডে সেট করা হয়েছে।
  }); 
});
